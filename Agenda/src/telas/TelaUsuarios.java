    private void consultar(){
        String sql = "SELECT * FROM contato WHERE id=?";
        try{
            pst= conexao.prepareStatement(sql);
            pst.setString(1, txtId.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                txtNome.setText(rs.getString(2));
                txtCelular.setText(rs.getString(3));
                txtEmail.setText(rs.getString(4));
                txtMes.setText(rs.getString(5));
            }else{
                JOptionPane.showMessageDialog(null, "Contato nÃ£o cadastrado");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    private void adicionar(){
        String sql = "INSERT INTO contato(nome, celular, email, mes) VALUES(?,?,?,?)";
        try{
            pst= conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtCelular.getText());
            pst.setString(3, txtEmail.getText());
            pst.setString(4, txtMes.getText());
            int adicionado= pst.executeUpdate();//retorna 1 se ok
            
            if(adicionado > 0){
                JOptionPane.showMessageDialog(null, "Usuario Cadastrado");
                //limpar
                txtNome.setText(null);
                txtCelular.setText(null);
                txtEmail.setText(null);
                txtMes.setText(null);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
